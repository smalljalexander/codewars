//Complete the solution so that it reverses the string passed into it.

//'world'  =>  'dlrow'
//'word'   =>  'drow'


//Parameters- str-take in a string of one word
//return- reverse the order of letters in the string
//examples- 'world' => 'dlrow', word => drow, dog => god
//psuedocode- take in a string of one word, reverse the order of the letters return it




function solution(str){
    return str.split('').reverse('').join('')
}
